/**
 * Get celebration content for a given level.
 * @param level - The new level achieved
 * @returns Level up content with formatted strings
 */
export function getLevelUpContent(level: number): LevelUpContent {
  let pool: LevelUpContent[];

  if (level <= 5) pool = NOVICE_CELEBRATIONS;
  else if (level <= 10) pool = APPRENTICE_CELEBRATIONS;
  else if (level <= 15) pool = ADEPT_CELEBRATIONS;
  else if (level <= 20) pool = EXPERT_CELEBRATIONS;
  else pool = MASTER_CELEBRATIONS;

  // Select content based on level (deterministic selection)
  const index = (level - 1) % pool.length;
  const content = pool[index];

  // Format {level} placeholder
  return {
    headline: content.headline.replace('{level}', level.toString()),
    subtitle: content.subtitle.replace('{level}', level.toString()),
    flavorText: content.flavorText,
  };
}

